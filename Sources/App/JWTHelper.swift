class JWTHelper {
    func sign(name: String, role: String) -> String? {
        do {
            let privateSigner = try JWTSigner.rs256(key: .private(pem: privateKeyString))
            
            let payload = TestPayload(
                sub: "vapor",
                name: name,
                role: role,
                admin: (name == "Marcel"),
                exp: .init(value: .init(timeIntervalSince1970: 2_000_000_000))
            )
            
            var jwt = JWT(payload: payload)
            let privateSigned = try jwt.sign(using: privateSigner)
            
            return String(data: privateSigned, encoding: .utf8)
        } catch {
            return nil
        }
    }
}
